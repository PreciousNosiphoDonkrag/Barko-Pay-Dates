--Company Level
SELECT 
'COMPANY' AS COMPANY,
'' AS REGION,
'' AS AREA,
'' AS BRANCH,
EMPLOYER_ID,
EMPLOYER,
'COMPANY' AS HIERARCHY_INDICATOR,
'COMPANY' AS HIERARCHY,
FREQUENCY AS PAY_FREQUENCY,

addDays(
    anchor_monday,
    intDiv(
        dateDiff('day', anchor_monday, toDate(NEXT_ACTION_DATE)),
        14
    ) * 14
) AS PERIOD_START,

addDays(
    addDays(
        anchor_monday,
        intDiv(
            dateDiff('day', anchor_monday, toDate(NEXT_ACTION_DATE)),
            14
        ) * 14
    ),
    13
) AS PERIOD_END,

SUM(CAPITAL_AMOUNT) AS CAPITAL_TOTAL,
COUNT(DISTINCT RECID_LOAN_ID) AS LOANS_COUNT,
COUNT(DISTINCT CO_CODE) AS ACTIVE_BRANCHES,
SUM(REPAYMENT_TOTAL) AS REPAYMENT_TOTAL,
groupArray(toDate(NEXT_ACTION_DATE)) AS NEXT_ACTION_DATES_ARRAY,

today() AS DATA_DATE,
now() AS LAST_UPDATE

FROM "REPLICATED_TABLES"."LENDING_TRANSACTIONS_REPL" FINAL

WHERE LOAN_STATUS IN ('CURRENT','EXPIRED')
AND FREQUENCY = 'FORTNIGHT' --AND YEAR(NEXT_ACTION_DATE) != 1970


GROUP BY
EMPLOYER_ID,
EMPLOYER,
FREQUENCY,
PERIOD_START,
PERIOD_END

