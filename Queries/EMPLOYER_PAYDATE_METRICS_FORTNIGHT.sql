  AREA,
  BRANCH,
  EMPLOYER_ID,
  EMPLOYER,
  HIERARCHY_INDICATOR,
  HIERARCHY,
  PAY_FREQUENCY,
  PERIOD_START,
  PERIOD_END,
  CAPITAL_TOTAL,
  LOANS_COUNT,
